package com.emergencyapp.firstActivity.createAct;

public class Create {

    private String Society_Name;
    private String Secretary_Name;
    private String House_Number;
    private String Phone_Number;

    public Create() {
    }

    public String getSociety_Name() {
        return Society_Name;
    }

    public void setSociety_Name(String society_Name) {
        Society_Name = society_Name;
    }

    public String getSecretary_Name() {
        return Secretary_Name;
    }

    public void setSecretary_Name(String secretary_Name) {
        Secretary_Name = secretary_Name;
    }

    public String getHouse_Number() {
        return House_Number;
    }

    public void setHouse_Number(String house_Number) {
        House_Number = house_Number;
    }

    public String getPhone_Number() {
        return Phone_Number;
    }

    public void setPhone_Number(String phone_Number) {
        Phone_Number = phone_Number;
    }
}
